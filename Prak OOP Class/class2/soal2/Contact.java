public class Contact {
    String Nama , email , nomer_telepon;

    Contact(){
        Nama = "";
        email = "";
        nomer_telepon = "";
    }
    
    Contact(String Nama , String email , String nomer_telepon){
        this.Nama = Nama;
        this.email = email;
        this.nomer_telepon = nomer_telepon;
    }
    
    
}
